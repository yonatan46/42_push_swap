#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*sa;

	sa = (char *)s;
	while (*sa != (char )c)
	{
		if (*sa == '\0')
			return (0);
		sa++;
	}
	return (sa);
}
